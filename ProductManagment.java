    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        int selectedColumn = 1;

        if (selectedRow >= 0 && selectedRow < dt.getRowCount()) {
            Object selectedValue = dt.getValueAt(selectedRow, selectedColumn);

            if (selectedValue != null) {
                String selectedName = selectedValue.toString();

                String eslesenIsim = null;
                Iterator<String> iterator = seleceteditemNames.iterator();
                while (iterator.hasNext()) {
                    String foodName = iterator.next();

                    if (foodName.equals(selectedName)) {
                        iterator.remove();
                        eslesenIsim = foodName;
                    }
                }

                if (selectedRow != -1 && dt.getColumnCount() > 0) {
                    int priceColumnIndex = -1;

                    for (int i = 0; i < dt.getColumnCount(); i++) {
                        if (dt.getColumnName(i).equalsIgnoreCase("Price")) {
                            priceColumnIndex = i;
                            break;
                        }
                    }

                    if (priceColumnIndex != -1) {
                        double deletedItemPrice = Double.parseDouble(dt.getValueAt(selectedRow, priceColumnIndex).toString());

                        dt.removeRow(selectedRow);

                        double currentTotal = Double.parseDouble(totalLabel.getText());
                        double newTotal = currentTotal - deletedItemPrice;
                        DecimalFormat df = new DecimalFormat("0.00");
                        totalLabel.setText(df.format(newTotal));
                    } else {
                        System.out.println("Price column not found.");
                    }

                }
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


